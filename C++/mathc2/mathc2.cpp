#include<mathc2.h>

int mathc2::addc(int a,int b)
{
	int c;
	c=a+b;
	return c;
}
int mathc2::subc(int a,int b)
{
	int c;
	c=a-b;
	return c;
}
int mathc2::mulc(int a,int b)
{
	int c;
	c=a*b;
	return c;
}
float mathc2::divc(int a,int b)
{
	float c;
	c=float(a/b);
	return c;
}
int mathc2::modc(int a,int b)
{
	int c;
	c=a%b;
	return c;
}
